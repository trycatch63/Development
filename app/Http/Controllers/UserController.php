    public function login(Request $request){

        $credentials = [
            'email' => $request['email'],
            'password' => $request['password'],
        ];

        if (Auth::attempt($credentials)) {
            $user=User::find(Auth::id());
            return response()->json([
                'person' => $user
            ]);

        }
        return response()->json([
            'person' => "Failure"
        ]);

    }
